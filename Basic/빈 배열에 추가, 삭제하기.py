def solution(arr, flag):
    answer = []
    for i in range(len(arr)):
        if (flag[i]):
            answer += str(arr[i]) * (arr[i]*2)
        else:
            si =len(answer) - arr[i]
            answer=answer[:si]
        
    a=[int(x) for x in answer]

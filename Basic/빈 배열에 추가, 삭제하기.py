# 🧠 배운 점 & 복습할 내용
# 문제에서 새로 알게 된 문법이나 테크닉을 적습니다.
# 다음에 비슷한 문제를 만난다면 어떻게 접근할지 한 줄로 요약합니다.


def solution(arr, flag):
    arr1 = []
    for i, j in zip(arr, flag):
        if j:
            arr1 += [i] * i * 2
        else:
            arr1 = arr1[:-i]
    return arr1

# zip()으로 묶어서 배열로 합친 후 i,j로 각각 할당함 
# 문자열로도 * 연산이 되지만 [i]로도 가능
# 인덱싱할 때 -i 같이해서 뒤에서부터 자르는 것도 가능!